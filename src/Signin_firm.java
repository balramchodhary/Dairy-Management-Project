    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fid=jTextField1.getText();
        String ownname=jTextField2.getText();
        String location=jTextField3.getText();
        int pin=Integer. parseInt(jTextField4.getText());
        int rate=Integer. parseInt(jTextField5.getText());
        String cname=jTextField6.getText();
        
        try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/dairy_m","root","mayank0118");
        Statement statement = connect.createStatement();
        ResultSet rset = statement.executeQuery("Select cname from company");
        boolean sign=false;
         while(rset.next()){
             if(cname.equals(rset.getString(1))){
                 sign=true;
                 break;
             }
         }
         if(!sign)
             JOptionPane.showMessageDialog(null, "Company name is not exist", "Failure", JOptionPane.ERROR_MESSAGE);
        
        if(sign){
        pstmt=(PreparedStatement)connect.prepareStatement("insert into local_firm values(?,?,?,?,?)");
        pstmt.setString(1,fid);
        pstmt.setInt(2,pin);
        pstmt.setString(3,location);
        pstmt.setInt(4,rate);
        pstmt.setString(5,ownname);
        pstmt.executeUpdate();
        
        pstmt=(PreparedStatement)connect.prepareStatement("insert into send_to values(?,?)");
        pstmt.setString(1,fid);
        pstmt.setString(2,cname);
        pstmt.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Your data successfully inserted!", "Welcome", JOptionPane.PLAIN_MESSAGE);
        }
        }catch(Exception EX){
            JOptionPane.showMessageDialog(null, "Sorry please try again", "Failure", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

