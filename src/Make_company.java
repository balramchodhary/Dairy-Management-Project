    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String cname=jTextField1.getText();
        String Ceoname=jTextField2.getText();
        String pass = String.valueOf(jPasswordField1.getPassword());
        String add=jTextField3.getText();
        int pin=Integer.parseInt(jTextField4.getText());

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/dairy_m","root","mayank0118");
            Statement statement = connect.createStatement();

            pstmt=(PreparedStatement)connect.prepareStatement("insert into company values(?,?,?,?,?)");

            pstmt.setString(1,cname);
            pstmt.setInt(2,pin);
            pstmt.setString(3,Ceoname);
            pstmt.setString(4,add);
            pstmt.setString(5,pass);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Your data successfully inserted!", "Welcome", JOptionPane.PLAIN_MESSAGE);
        }catch(Exception EX){
            JOptionPane.showMessageDialog(null, "Sorry please try again", "Failure", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

